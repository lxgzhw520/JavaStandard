package com.lxgzhw520.day001;

public class Demo01DataType {
    public static void main(String[] args) {
        //自动类型转换
        System.out.println(1033);
        System.out.println(44.444);

        //自动类型转换适合于小类型转大类型
        long num1 = 100;
        System.out.println(num1);

        double num2 = 2.5F;
        System.out.println(num2);
    }
}
